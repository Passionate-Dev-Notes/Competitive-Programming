package checkNegativeOrPosetive;

public class NegativeOfPositiveLogic {
	public void isNegativeOrPositive(int data) {
		if(data<0) {
			System.out.println("It's negative "+data);
		}
		else if(data>0) {
			System.out.println("It's posetive Number "+data);
		}
		else
			System.out.println("It's equal to zero");
		
	}

}
